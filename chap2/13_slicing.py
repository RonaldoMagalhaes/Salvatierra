friends = ["Rolf", "Charlie", "Anna", "Bob", "Jen"]

print(friends[1:3])  # ['Charlie', 'Anna']
print(friends[1:])  # ['Charlie', 'Anna', 'Bob', 'Jen']

print(friends[::-1])  # ['Jen', 'Bob', 'Anna', 'Charlie', 'Rolf']
print(friends[:])  # get a new list with the same elements

print(friends[-3:])  # ['Jen', 'Bob', 'Anna', 'Charlie', 'Rolf']

